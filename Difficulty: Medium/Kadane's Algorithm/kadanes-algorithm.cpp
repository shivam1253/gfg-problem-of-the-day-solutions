class Solution {
  public:
    // Function to find the sum of contiguous subarray with maximum sum.
    int maxSubarraySum(vector<int> &arr) {
        // code here...
        int ans=0;
        int sum=INT_MIN;
        int n=arr.size();
        for(int i=0; i<n; i++){
            ans+=arr[i];
            sum=max(ans,sum);
            if(ans<0) ans=0;
            }
            return sum;
    }
};

