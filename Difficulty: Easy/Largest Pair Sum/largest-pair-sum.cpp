class Solution {
  public:
    int pairsum(vector<int> &arr) {
        int p=INT_MIN;
        for(auto it:arr){
            p=max(p,it);
        }
        int q=INT_MIN;
        for(auto it:arr){
            if(it!=p){
                q=max(q,it);
            }
        }
        return p+q;
        // code here
    }
};


